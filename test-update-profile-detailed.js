// First, let's get a list of all users in the database to see what emails exist
const testGetUsers = async () => {
  try {
    console.log('\n🔍 Checking backend health and available users...\n');
    
    // Check basic connectivity
    const response = await axios.get('https://safe-net-one.vercel.app/api/check-email?email=test@test.com');
    console.log('✅ Backend is responding');
    console.log('Response:', response.data);
  } catch (error) {
    console.error('❌ Error:', {
      message: error.message,
      status: error.response?.status,
      data: error.response?.data
    });
  }
};

// Test with actual user signup first (to create a test user)
const testSignup = async () => {
  try {
    console.log('\n📝 Testing signup...\n');
    
    const response = await axios.post('https://safe-net-one.vercel.app/api/auth/signup', {
      name: 'Debug TestUser',
      phone: '9999999999',
      email: 'debug-test@test.com',
      password: 'Test123!',
      location: {
        latitude: 0,
        longitude: 0
      }
    }, { timeout: 15000 });
    
    console.log('✅ Signup successful:', response.data);
    return response.data.user?.email;
  } catch (error) {
    if (error.response?.status === 400 && error.response?.data?.message === 'User already exists') {
      console.log('⚠️ User already exists, continuing with test...');
      return 'debug-test@test.com';
    }
    console.error('❌ Signup error:', {
      message: error.message,
      status: error.response?.status,
      data: error.response?.data
    });
    throw error;
  }
};

// Test update-profile without file
const testUpdateProfileNoFile = async (email) => {
  try {
    console.log('\n🔄 Testing update-profile (no file)...\n');
    
    const form = new FormData();
    form.append('currentEmail', email);
    form.append('name', 'Updated Name');
    form.append('phone', '1111111111');
    
    const response = await axios.post(
      'https://safe-net-one.vercel.app/api/auth/update-profile',
      form,
      {
        headers: form.getHeaders(),
        timeout: 15000
      }
    );
    
    console.log('✅ Update (no file) successful:', response.data);
    return true;
  } catch (error) {
    console.error('❌ Update (no file) error:', {
      message: error.message,
      status: error.response?.status,
      data: error.response?.data
    });
    return false;
  }
};

